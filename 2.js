"use strict";
var animals = ['Dog', 'Cat', 'Rabbit'];
// Loop to print each animal name
for (let animal of animals) {
    console.log(`${animal}`);
}
// Loop to print a statement about each animal
for (let animal of animals) {
    console.log(`A ${animal.toLowerCase()} will be a great pet.`);
}
// Statement about what the animals have in common
console.log("Any of these animals will be a great pet!");
